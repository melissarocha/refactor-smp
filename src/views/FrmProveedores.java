    private void cmdAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAccederActionPerformed
        
        if (Proveedor.isValidString(txtNombre.getText())
                && Proveedor.isValidString(txtDireccion.getText()) 
                && Proveedor.isNumeric(txtTelefono.getText())
                && Proveedor.isValidString(txtCiudad.getText())) {
            
            nombreProveedor = txtNombre.getText();
            direccionProveedor = txtDireccion.getText();
            telefono = txtTelefono.getText();
            ciudad = txtCiudad.getText();
            
            Proveedores.executeQuery(Conexion.getInstance().getCon(), String.format("insert into proveedores(id_proveedor, nombre_proveedor, direccion_proveedor, telefono, ciudad) values(%s, '%s', '%s', %s, '%s')", id, nombreProveedor, direccionProveedor, telefono, ciudad));
            JOptionPane.showMessageDialog(rootPane, "A√±adido exitosamente!");
            txtCiudad.setText(Proveedor.VACIO);
            txtNombre.setText(Proveedor.VACIO);
            txtDireccion.setText(Proveedor.VACIO);
            txtTelefono.setText(Proveedor.VACIO);
            
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Invalido");
        }
        
        
        
    }//GEN-LAST:event_cmdAccederActionPerformed

